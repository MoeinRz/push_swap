#include "../include/push_swap.h"

//===========================================================================//
//initializing the s                                                         //
//===========================================================================//
void	init_stack(t_stack *s, int size)
{
	s->t_stack = (int *)malloc(size * sizeof(int));
	if (s->t_stack == NULL)
	{
		write(1, "Error\n", 6);
		exit(1);
	}
	s->top = -1;
	s->size = size;
	s->count = 0;
}

//===========================================================================//
//make s free                                                                //
//===========================================================================//
void	free_stack(t_stack *s)
{
	free(s->t_stack);
	s->t_stack = NULL;
	s->top = -1;
	s->size = 0;
}

//===========================================================================//
//check the s is empty or not                                                //
//===========================================================================//
int	is_empty(t_stack *s)
{
	if (s->top == -1)
		return (-1);
	else
		return (s->top);
}

//===========================================================================//
//check the s is full or not                                                 //
//===========================================================================//
int	is_full(t_stack *s)
{
	return (s->top == s->size - 1);
}

//===========================================================================//
//free the split                                                             //
//===========================================================================//
void	free_split(char **split)
{
	int	i;

	i = 0;
	while (split[i])
	{
		free(split[i]);
		i++;
	}
	free(split);
}
