#include "../include/push_swap.h"

//===========================================================================//
//rrr                                                                        //
//===========================================================================//
void	rrr(t_stack *a, t_stack *b, int display)
{
	rra(a, 0);
	rrb(b, 0);
	b->count--;
	if (display)
		write(1, "rrr\n", 4);
}
